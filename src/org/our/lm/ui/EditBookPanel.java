         private void PrintTable(ResultSet rs)                            {
                  int i=0;
                  try
                  {             if(!rs.next())
                                {
                                        ColourMessage("There is No Matching book", 0,lbl_SearchError);
                                        SetBlankOnBookEditWindow();
                                }
                                else
                                {
                                        do
                                        {

                                                      searchBookResult.setValueAt(rs.getString("bookregno"),i,0);
                                                      searchBookResult.setValueAt(rs.getString("bookname"), i,1);
                                                      searchBookResult.setValueAt(rs.getString("authorname"),i,2);
                                                      searchBookResult.setValueAt(rs.getString("publishername"), i,3);
                                                      searchBookResult.setValueAt(rs.getString("bookedtion"),i ,4 );
                                                      searchBookResult.setValueAt(rs.getString("catogory"), i,5);
                                                      i++;
                                        }while(rs.next());
                                        BookSearchResultTableWindow.setVisible(true);
                                        ColourMessage("", 0,lbl_SearchError);
                                }
                   }
                   catch(Exception e)
                   {
                                System.out.println(e.getMessage());
                                e.printStackTrace();
                   }

    }
