       private void saveButtonInEditBook(){

                boolean mandatory =false;
                if(txt_RegNo1.getText().equals(""))
                        mandatory=true;
                if(txt_BName1.getText().equals(""))
                        mandatory=true;
                try
                {
                     float aVal=Integer.parseInt(txt_Price1.getText());
                }
                catch(NumberFormatException nfe)
                {

                    mandatory=true;
                }
                if(mandatory)
                            ColourMessage("Some of the mandatory fields were not filled. Please review your input", 0,lbl_ErrorMesgInBEditWindow);
                else
                {
                            Book book=new Book();
                            book.setBookRegisterNo(txt_RegNo1.getText());book.setBookName(txt_BName1.getText());
                            book.setAuthorName(txt_Author1.getText());book.setPublisherName(txt_Publi1.getText());
                            book.setPublishedYear(txt_PYear1.getText());book.setPrice(txt_Price1.getText());
                            book.setPurchaseDate(txt_Pdate1.getText());book.setISBN(txt_Isbn1.getText());
                            book.setBookSource(txt_BSource1.getText());book.setBookEdition(txt_Edition1.getText());
                            book.setCatogory(txt_Catogory1.getText());book.setBookLanguage(txt_Langu1.getText());
                            book.setShelfNumber((String)combo_ShelfNo.getSelectedItem());
                            querymanager.UpdateBook(book);
                            SetBlankOnBookEditWindow();
                            ColourMessage("Update Successfully", 1,lbl_ErrorMesgInBEditWindow);
                }
       }
