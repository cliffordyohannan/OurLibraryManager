    private void bt_SaveInIncomeNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SaveInIncomeNewActionPerformed
        boolean mandatory=false;
        if(txt_receivedFrom.getText().equals(""))
            mandatory=true;
        if(txt_amount.getText().equals(""))
            mandatory=true;
        else {
            try {
                int amount=Integer.parseInt(txt_amount.getText());
            } catch(Exception e) {
                mandatory=true;
            }
        }
        if(mandatory) {
            ColourMessage("Some of the mandatory fields were not filled. Please review your input",0,lbl_ErrorInIncome);
        } else {
            Income income=new Income();
            incomeCatogory=this.getIncomeCatogoriesFromResultSet();
            income.setDate(combo_YYInIncomeNew.getSelectedItem()+"-"+combo_MMInIncomeNew.getSelectedItem()+"-"+combo_DDInIncomeNew.getSelectedItem());
            income.setReceiptNo(lbl_receiptNo.getText());income.setReceivedFrom(txt_receivedFrom.getText());
            income.setDescription(txt_description.getText());income.setAmount(txt_amount.getText());income.setAmount(txt_amount.getText());
            income.setCategoryId(""+setCatogoryIdForIncome(incomeCatogory));
            querymanager.ImportIncome(income);
            ColourMessage("Succesfully added", 1,lbl_ErrorInIncome);
            int retValue=1+querymanager.getLastInsertedReceptNo();
            lbl_receiptNo.setText(""+retValue);
            ClearIncomeWindow();
        }
}//GEN-LAST:event_bt_SaveInIncomeNewActionPerformed
