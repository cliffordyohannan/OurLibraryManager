    private void bt_SaveInAssetEditWindowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SaveInAssetEditWindowActionPerformed
        if(this.testManadatoryInAssetEditWindow())
            ColourMessage("Some of the mandatory fields were not filled. Please review your input", 0, lbl_ErrorInAssetEdit);
        else {
            Asset asset=this.getValuesFromAssetEditWindow();
            int returnUp=querymanager.UpdateAsset(asset);
            if(returnUp==0) {
                ColourMessage("Incorrect data value", 0, lbl_ErrorInAssetEdit);
                asset=null;
            } else {
                this.setBlankOnAssetEditWindow();
                ColourMessage("Succesfully Completed", 1, lbl_ErrorInAssetEdit);
                asset=null;
            }


        }
}//GEN-LAST:event_bt_SaveInAssetEditWindowActionPerformed
private Asset        getValuesFromAssetEditWindow()                       {
           Asset asset=new Asset();
           asset.setDate(txt_AssetDate.getText());asset.setAssetId(lbl_AssetEditId.getText());asset.setName(txt_AssetEditName.getText());
           asset.setDescription(txt_AssetEditDescription.getText());asset.setCategory((String)combo_AssetEditCategory.getSelectedItem());
           asset.setAmount(txt_AssetEditAmount.getText());asset.setNoItems(txt_AssetEditNoofItems.getText());asset.setRemarks(txt_AssetEditRemarks.getText());
           if(rbt_AssetEditAcquired.isSelected())
                  asset.setIsRemoved("1");
           else
                  asset.setIsRemoved("0");
           return asset;
       }
