    private void bt_OkAssetSearchResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_OkAssetSearchResultActionPerformed
        if(table_AssetSearchResult.getSelectedRow()<0)
            ColourMessage("Please select a row", 0, lbl_errorInAssetSearchTable);
        else
            if(table_AssetSearchResult.getValueAt(table_AssetSearchResult.getSelectedRow(),0)!=null) {
                String searchKey=(String)table_AssetSearchResult.getValueAt(table_AssetSearchResult.getSelectedRow(), 0);
                ResultSet rs=querymanager.SearchAsset(searchKey,0);
                this.setValuesOnAssetEditWindow(rs);
                AssetSearchResultTableWindow.setVisible(false);
            } else
                ColourMessage("Please select a valid row", 0, lbl_errorInAssetSearchTable);
}//GEN-LAST:event_bt_OkAssetSearchResultActionPerformed

