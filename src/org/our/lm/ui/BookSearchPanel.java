    private void txt_SearchKeyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_SearchKeyKeyReleased
       this.clearTable(searchBookResult, searchBookResult.getRowCount(),searchBookResult.getColumnCount());
        String serchType=(String)combo_SearchKey.getSelectedItem();
        String upperCase=serchType.toUpperCase();
       
        if(upperCase.equals("NAME"))
        {
            
            ResultSet rs=queryManager.SearchBook(txt_SearchKey.getText(), 1);
            this.PrintTable(rs);


        }
        else 
        if(upperCase.equals("CATEGORY"))
        {
            ResultSet rs=queryManager.SearchBook(txt_SearchKey.getText(), 2);
            this.PrintTable(rs);
        }
        else
        if(upperCase.equals("AUTHOR"))
        {
            ResultSet rs=queryManager.SearchBook(txt_SearchKey.getText(), 3);
            this.PrintTable(rs);
        }
    }//GEN-LAST:event_txt_SearchKeyKeyReleased

