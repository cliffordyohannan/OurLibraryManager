    private void PrintTable(ResultSet rs) {
    int i=0;
                  try
                  {
                      
                     
                      if(!rs.next())
                                {
                                        ColourMessage("There is No Matching book", 0,lbl_Msg);
                                }
                                else
                                {
                                        do
                                        {

                                            
                                                      searchBookResult.setValueAt(rs.getString("bookregno"),i,0);
                                                      searchBookResult.setValueAt(rs.getString("bookname"), i,1);
                                                      searchBookResult.setValueAt(rs.getString("authorname"),i,2);
                                                      searchBookResult.setValueAt(rs.getString("shelfno"), i,3);
                                                      searchBookResult.setValueAt(rs.getString("catogory"),i ,4 );
                                                      searchBookResult.setValueAt(rs.getString("bookedtion"), i,5);
                                                      searchBookResult.setValueAt(rs.getString("price"), i,6);
                                                      if(rs.getInt("isreturned")==1)
                                                      {
                                                          searchBookResult.setValueAt("Available", i, 7);
                                                      }
                                                      else
                                                          searchBookResult.setValueAt("Not Available", i, 7);
                                                      
                                                      i++;
                                        }while(rs.next());
                                     
                                        ColourMessage("", 0,lbl_Msg);
                                }
                   }
                   catch(Exception e)
                   {
                                System.out.println(e.getMessage());
                                e.printStackTrace();
                   }

    }
    private void clearTable(JTable tablename,int rowcount,int columcount){
        for(int i=0;i<rowcount;i++)
        for(int j=0;j<columcount;j++)
            tablename.setValueAt("",i, j);
    }
