    private void bt_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_saveActionPerformed
        if(txt_fname.getText().equals(""))
            ColourMessage("Please enter a valid file name",0,lbl_Msg);
        else {
            MemberReport memberreport=new MemberReport();
     
            memberreport.setReportDate1((String)comp_yy.getSelectedItem()+"-"+(String)combo_mm.getSelectedItem()+"-"+(String)combo_dd.getSelectedItem());
            memberreport.setReportDate2((String)comb_YY.getSelectedItem()+"-"+(String)comb_MM.getSelectedItem()+"-"+(String)comb_DD.getSelectedItem());
            String FileName=txt_fname.getText();
            if(combo_type.getSelectedItem().equals(".pdf")) {
                String fileName=("D:/"+FileName+".pdf");
                Pdformat pdfcreater=new Pdformat();
                ResultSet rs=querymanager.getAllMemberDetails(memberreport);
                pdfcreater.createTable(rs,fileName);
                ColourMessage("Successfully Saved to :"+fileName,1,lbl_Msg);
     
     
            }
            else
            {
            }
     
     
     
     
     
           
     
        }
    }//GEN-LAST:event_bt_saveActionPerformed

