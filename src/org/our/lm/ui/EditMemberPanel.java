       private void saveButtonInEditMember()       {
            if(this.testMandatoryInMemberEditWindow())
            {
                    ColourMessage("Some of the mandatory fields were not filled. Please review your input", 0, lbl_ErrorInSaveInEditMember);
            }
            else
            {
                    Member member=this.getValuesFromMemberEditWindow();
                    querymanager.UpdateMember(member);
                    ColourMessage("Update Successfully",1, lbl_ErrorInSaveInEditMember);
                    setBlankOnMemberEditWindow();
            }
       }
