          private void SetValuesOnEditMemberWindow(ResultSet rs)           {
            try
            {
                  if(!rs.next())
                  {
                           ColourMessage("Member not exist",0,lbl_ErrorInMemberEditSearch);
                           setBlankOnMemberEditWindow();

                  }
                  else
                  {
                            do
                            {
                                        txt_memberId1.setText(rs.getString("memberid"));txt_FName1.setText(rs.getString("memberfname"));txt_LName1.setText(rs.getString("memberlname"));
                                        combo_Gender1.setSelectedItem(rs.getString("gender"));txt_DOB1.setText(rs.getString("dob"));txt_HName1.setText(rs.getString("housename"));
                                        txt_LMark1.setText(rs.getString("landmark"));txt_Po1.setText(rs.getString("postoffice"));combo_Panchayath1.setSelectedItem(rs.getString("panchayath"));
                                        txt_PRes1.setText(rs.getString("phone1"));txt_PMob1.setText(rs.getString("phone2"));txt_Pin1.setText(rs.getString("pincode"));txt_memberJoinDate.setText(rs.getString("joindate"));
                                        if(rs.getString("membertype").equals("1"))
                                                rbt_Perm1.setSelected(true);
                                        else
                                                rbt_Temp1.setSelected(true);
                                        if(rs.getString("ispaid").equals("1"))
                                                rbt_Paid1.setSelected(true);
                                        else
                                                rbt_Paid1.setSelected(false);
                                        txt_AFee1.setText(rs.getString("admissionfee"));


                            }while(rs.next());
                             ColourMessage("",0,lbl_ErrorInMemberEditSearch);
                  }
            }catch(Exception e)
            {
                e.printStackTrace();
            }
    }
 private void searchResultOfMemberIntable(ResultSet rs)           {
            int i=0;
            try
            {
                if(!rs.next())
                {
                    ColourMessage("Member not exist", 0,lbl_ErrorInMemberEditSearch);
                    setBlankOnMemberEditWindow();
                }
                else
                {
                            do
                            {
                                table_MemberSearchResult.setValueAt(rs.getString("memberid"), i, 0);
                                table_MemberSearchResult.setValueAt(rs.getString("memberfname"), i, 1);
                                table_MemberSearchResult.setValueAt(rs.getString("memberlname"),i, 2);
                                table_MemberSearchResult.setValueAt(rs.getString("gender"), i, 3);
                                table_MemberSearchResult.setValueAt(rs.getString("dob"), i, 4);
                                table_MemberSearchResult.setValueAt(rs.getString("housename"), i, 5);
                                table_MemberSearchResult.setValueAt(rs.getString("postoffice"),i, 6);
                                table_MemberSearchResult.setValueAt(rs.getString("pincode"), i, 7);
                                table_MemberSearchResult.setValueAt(rs.getString("phone1"), i, 8);
                                table_MemberSearchResult.setValueAt(rs.getString("phone2"), i, 9);

                                i++;

                            }while(rs.next());
                            ColourMessage("", 0,lbl_ErrorInMemberEditSearch);
                            MemberSearchResultTableWindow.setVisible(true);
                }

            }
            catch(Exception e)
            {
                    e.printStackTrace();
            }

    }
